var id_counter = 0;
function addId(data) {
  for (var i=0; i<data.length; i++) {
    data[i].id = id_counter++;
    if (data[i].children) addId(data[i].children);
  }
}

function addI(data, start_at) {
  start_at = start_at || 0;
  for (var i=0; i<data.length; i++) {
    data[i].i = start_at+i;
    if (data[i].children) addI(data[i].children, data.length);
  }
}
